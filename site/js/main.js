function detail(id) {
  const d = P[id]; if(!d) return;
  prevPage = document.querySelector('.page.active').id.replace('page-','');

  const blocks = d.blocks.map(b => {
    const inner = b.tech
      ? `<div class="pills">${b.items.map(i=>`<span class="pill">${i}</span>`).join('')}</div>`
      : `<ul class="d-list">${b.items.map(i=>`<li>${i}</li>`).join('')}</ul>`;
    return `<div class="d-block"><div class="d-block-title">${b.t}</div>${inner}</div>`;
  }).join('');

  const links = d.links.length
    ? `<div class="d-links">${d.links.map(l=>`<a href="${l.u}" target="_blank" class="btn btn-gold">↗ ${l.l}</a>`).join('')}</div>`
    : `<p style="color:var(--text3);font-style:italic;margin-top:1.2rem;">Ссылки появятся после релиза</p>`;

  document.getElementById('detail-content').innerHTML = `
    <div class="d-hero">
      <div style="display:flex;gap:0.6rem;flex-wrap:wrap;margin-bottom:1.2rem;">
        <span class="tag ${d.tc}">${d.tag}</span>
        <span class="sbadge"><span class="sdot ${d.sc}"></span>${d.tag}</span>
      </div>
      <div style="font-size:3.2rem;margin-bottom:0.8rem;color:${d.ic}">${d.icon}</div>
      <h1 class="d-title">${d.title}</h1>
      <p class="d-sub">${d.sub}</p>
      <p class="d-desc">${d.desc}</p>
      ${links}
    </div>
    <div class="d-blocks">${blocks}</div>
  `;
  show('detail');
}

/* ── FLOATING RUNES ── */
(function spawnRunes(){
  const C = document.getElementById('rune-field');
  if (!C) return;
  const R = ['ᚠ','ᚢ','ᚦ','ᚨ','ᚱ','ᚲ','ᚷ','ᚹ','ᚾ','ᛁ','ᛈ','ᛉ','ᛊ','ᛏ','ᛒ','ᛖ','ᛗ','ᛚ','ᛞ','ᛟ','☠','✦','⚒'];
  setInterval(() => {
    const e = document.createElement('div');
    e.className = 'floating-rune';
    e.textContent = R[Math.floor(Math.random()*R.length)];
    e.style.cssText = `left:${Math.random()*100}%;top:${35+Math.random()*55}%;font-size:${0.7+Math.random()*1.4}rem;animation-duration:${7+Math.random()*7}s;animation-delay:0s;`;
    C.appendChild(e);
    setTimeout(() => e.remove(), 14000);
  }, 750);
})();

